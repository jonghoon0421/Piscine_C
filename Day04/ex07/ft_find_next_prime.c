int		ft_find_next_prime(int nb)
{
	int		i;
	int		test;

	i = 2;
	test = nb + 1;
	if (nb <= 1)
		return (2);
	while (test > i)
	{
		if (test % i != 0)
			i++;
		else
			test++;
	}
	return (test);
}
