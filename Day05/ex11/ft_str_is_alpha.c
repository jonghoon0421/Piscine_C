int		ft_str_is_alpha(char *str)
{
	while (1)
	{
		if (!((*str <= 90 && *str >= 65) || (*str <= 122 && *str >= 97)))
			break ;
		if (*str == '\0')
			break ;
		str++;
	}
	if ((*str <= 90 && *str >= 65) && (*str <= 122 && *str >= 97))
		return (1);
	else if (*str == '\0')
		return (1);
	else
		return (0);
}
